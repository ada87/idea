package com.xdnote.ep3;

public class Corrd {
	private int x = 0;
	private int y = 0;
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public Corrd(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}
	

}
